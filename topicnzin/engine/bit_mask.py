# -*- coding:utf-8 -*-
class BitMask(object):    
    @staticmethod
    def use_all():
        return BitMask.food() | BitMask.default() | BitMask.local() | BitMask.shop() | BitMask.verb() \
          | BitMask.mood() | BitMask.who() | BitMask.true() | BitMask.false() | BitMask.day() 
    
    @staticmethod
    def food():
        return 1
    
    @staticmethod
    def default():
        return 2
    
    @staticmethod
    def head():
        return 4
    
    @staticmethod
    def local():
        return 8
    
    @staticmethod
    def shop():
        return 16
    
    @staticmethod
    def tail():
        return 32
    
    @staticmethod
    def mood():
        return 64
    
    @staticmethod
    def verb():
        return 128
    
    @staticmethod
    def who():
        return 256
    
    @staticmethod
    def day():
        return 512
    
    @staticmethod
    def true():
        return 1024
    
    @staticmethod
    def false():
        return 2048
    
    @staticmethod
    def name():
        return 4096
    
    